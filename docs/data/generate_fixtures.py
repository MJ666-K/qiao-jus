def write_law(path: Path, title: str, domain: str, articles: list[tuple[str, str]]) -> None:
    lines = [f"# {title}", "【法律层级】法律", f"【领域】{domain}", ""]
    for no, text in articles:
        lines.append(no)
        lines.append(text)
        lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


